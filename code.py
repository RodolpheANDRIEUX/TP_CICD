"""Module mathématique simple."""


class SimpleMath:
    """Classe contenant des opérations mathématiques."""

    @staticmethod
    def addition(a, b):
        """Retourne la somme de a et b."""
        return a + b

    @staticmethod
    def soustraction(a, b):
        """Retourne la différence entre a et b."""
        return a - b
